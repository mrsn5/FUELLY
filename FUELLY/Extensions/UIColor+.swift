import UIKit

extension UIColor {
    static func primary() -> UIColor { return #colorLiteral(red: 0.9607843137, green: 0.8549019608, blue: 0.2901960784, alpha: 1) }
    
    static func primaryLight() -> UIColor { return #colorLiteral(red: 1, green: 0.9793802384, blue: 0.8694081764, alpha: 1) }
}
